        for (addr = A_MAX; addr >= 0x0; addr--)
        {
            set_mem_window(tag_acces_params_CD[addr].window);
            CACHE_DATA_LINE_BY_EA(tag_acces_params_CD[addr].addr0,tmp_var);//MSB
            CACHE_DATA_LINE_BY_EA(tag_acces_params_CD[addr].addr1,tmp_var);//LSB
        }
        rumboot_printf("Fill SRAM_EF tag array\n");
        test_event(TEC_CHECK_SRAM_EF);
        for (addr = A_MAX; addr >= 0x0; addr--)
        {
            set_mem_window(tag_acces_params_EF[addr].window);
            CACHE_DATA_LINE_BY_EA(tag_acces_params_EF[addr].addr0,tmp_var);//MSB
            CACHE_DATA_LINE_BY_EA(tag_acces_params_EF[addr].addr1,tmp_var);//LSB
        }
