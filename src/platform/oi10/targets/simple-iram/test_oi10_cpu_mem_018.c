int main()
{
    volatile uint32_t data;

    test_event_send_test_id("test_oi10_cpu_mem_018");

    rumboot_memfill8_modelling((void*)SRAM0_BASE, 0x8000, 0x00, 0x00); //workaround (init 32KB SRAM0)

    static const tlb_entry tlb_entries[] =
       {
        {TLB_ENTRY0_INV}, {TLB_ENTRY1_INV}, {TLB_ENTRY2_INV}, {TLB_ENTRY3_INV},
        {TLB_ENTRY4_INV}, {TLB_ENTRY5_INV}, {TLB_ENTRY6_INV}, {TLB_ENTRY7_INV},
        {TLB_ENTRY0_CACHE}, {TLB_ENTRY1_CACHE}, {TLB_ENTRY2_CACHE}, {TLB_ENTRY3_CACHE},
        {TLB_ENTRY4_CACHE}, {TLB_ENTRY5_CACHE}, {TLB_ENTRY6_CACHE}, {TLB_ENTRY7_CACHE},
       };


    emi_init(DCR_EM2_EMI_BASE);

    data = l2c_l2_read (DCR_L2C_BASE, L2C_L2ISTAT);
    uint32_t i = 0;
    while (((data & 0x1) != 1) && (i < L2C_TIMEOUT))
    {
        data = l2c_l2_read (DCR_L2C_BASE, L2C_L2ISTAT);
        i++;
    }
    TEST_ASSERT((data & 0x1),"L2C Array Initialization Complete Event did not occur!");

    rumboot_printf("Start TLB entries initialization... ");
    write_tlb_entries(tlb_entries, ARRAY_SIZE(tlb_entries));
    msync();
    isync();
    rumboot_printf("done\n");

    //invalidate all. emi_init causes a caching, we must clear it
    dci(0);
    dci(2);
    msync();

//    START TEST
//    1. Write 00..0
    fill_tag_sram(tag_value_type_zero ,addr_order_0_to_80);
//    2. Read 00..0
//       Write 11..1
//       Read 11..1
    read_tag_sram(tag_value_type_zero,addr_order_0_to_80);
    fill_tag_sram(tag_value_type_one,addr_order_0_to_80);
    read_tag_sram(tag_value_type_one,addr_order_0_to_80);
//    3. Read 11..1
//       Write 00..0
//       Read 00..0
    read_tag_sram(tag_value_type_one,addr_order_80_to_0);
    fill_tag_sram(tag_value_type_zero,addr_order_80_to_0);
    read_tag_sram(tag_value_type_zero,addr_order_80_to_0);
//    4. Read 00..0
    read_tag_sram(tag_value_type_zero,addr_order_80_to_0);

    rumboot_printf("TEST OK\n");
    return 0;
}
